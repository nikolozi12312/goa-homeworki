# 1. Output every item from list with positive indexing.

list1 = [2, 51, 11, 13, 51, 100]
#        0  1   2   3   4    5

print(list1[0], list1[1], list1[2], list1[3], list1[4], list1[5])

# 2. Output every item from list with negative indexing.

list1 = [2, 51, 11, 13, 51, 100]
#        1   2   3   4  5  6

print(list1[-6], list1[-5], list1[-4], list1[-3], list1[-2], list1[-1])

